    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // update user
        if (user != null) {
            System.out.println("key: " + user.getKey());
            user.setUser(txtUsu.getText());
            user.setPassword(txtSenha.getText());
            MenuInterface.sendUserRequest(new Request(MenuInterface.operation, user, this.txtUsu.getText()));

        } else {
            // Create user
            MenuInterface.sendUserRequest(new Request(MenuInterface.operation, new User(this.txtUsu.getText(), this.txtSenha.getText()), this.txtUsu.getText()));
        }
        
        user = null;
        this.setVisible(false);
        try {
            this.finalize();
        } catch (Throwable e) {
            System.out.println("Erro ao encerrar NewClient: " + e.getMessage());
        }
    }//GEN-LAST:event_btnOKActionPerformed

